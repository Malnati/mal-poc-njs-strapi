'use strict';

var log = require('../utils/log');

module.exports = function (encoding) {
  return function (_ref) {
    var options = _ref.options,
        res = _ref.res,
        body = _ref.body,
        raw = _ref.raw;


    raw = body;

    body = Buffer.from(body).toString(encoding);
    log({ body });

    return { options, res, body, raw };
  };
};